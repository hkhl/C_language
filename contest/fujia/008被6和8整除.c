//100到200间同时被6和8整除的数之和
#include<stdio.h>

int main()
{
    int sum = 0;
    for(int i = 100; i <= 200; i++)
    {
        if(!(i%6) && !(i%8))
        {
            sum += i;
        }
    }

    printf("%d", sum);
    return 0;
}


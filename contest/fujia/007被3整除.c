//个位是6 且能被3整除的5位数由几个
#include<stdio.h>

int main()
{
    int n = 0;
    for(int i = 10006; i <= 99996; i++)
    {
        if(!(i%3))
        {
            n++;
        }
    }
    printf("%d", n);

    return 0;
}

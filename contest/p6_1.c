#include<stdio.h>
int main()
{
    double k , f ,c;
    printf("请输入华式温度：");
    scanf("%lf", &f);
    c = 5 * (f - 32)/9; //摄氏温度
    k = 273.16 + c;     //绝对温度
    printf("摄氏温度：%.3lf，绝对温度：%.3lf", c, k);
    return 0;
}
